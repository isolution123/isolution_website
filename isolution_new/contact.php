<script type="text/javascript">
    $(function(){
        $("#submit").click(function(){
            // $("#submit").html('submitting ...');
            $("#submit").prop('disabled', true);
            var name = $("#name").val();
            var email = $("#email").val();
            var number = $("#number").val();
            var subject = $("#subject").val();
            var msg = $("#msg").val();
            if (name=='' || email=='' || number=='' || subject=='')
            {
                $("#submit").prop('disabled', false); 
                $('.success').show();
                $('.success').text("Please Fill All The Fields");                
            }
            else if(number.length<10)
            {
              $('.success').show();
              $('.success').text("Please Enter Valid Mobile Number");
              $("#submit").prop('disabled', false); 
            }
            else
            {
                $.ajax({
                    type:"post",
                    url:"submit_contact.php",
                    data:{name:name,email:email,number:number,subject:subject,msg:msg},
                    success:function(data){
                        $('.success').show();
                        $('.success').text(data);
                        // $("#submit").html('submit');                  
                        $("#submit").prop('disabled', false);    
                        $("#name").val('');
                        $("#email").val('');
                        $("#number").val('');
                        $("#subject").val('');
                        $("#msg").val('');
                    }                             

                });                            
            }
            return false;
        });
    });           
</script>
